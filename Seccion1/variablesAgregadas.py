#List: es una coleccion que esta ordenada y cambiable.Permite duplicar miembros
miList=["Manzana", "pera", "uvas"]
print(miList)

#Tuple: es una coleccion que esta ordenada e inmutable.Permite duplicar miembros
miTupla=("Manzana", "pera", "uvas", "uvas")
print(miTupla)


miSet={"Manzana", "pera", "uvas", "uvas"}
print(miSet)

#diccionario: es una coleccion que no esta ordenada, modificable e indexada
miDiccionario={
    "rojo":"manzana",
    "verde":"pera",
    "morado":"uva"
}
print(miDiccionario)